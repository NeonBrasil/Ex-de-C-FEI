public class variaveis_float {
    public static void main(String[] args)
    {
    //criar variaveis
        float f_variavel = 13.6654f;
        double d_variavel = 16.5484564981d;

        char x = 'j';
        boolean b = true;



    //printar variaveis
        System.out.println("Float = " +f_variavel);
        System.out.println("Double = " +d_variavel);
        System.out.println("boolean = " +b);
        System.out.println("Char = " +x);


    }
}
