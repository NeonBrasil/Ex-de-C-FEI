public class factorialNumber {
    
    public static void main (String[]args){

        //calcular o fatorial do 10
        // 10! = 10*9*8*7*6*5...*1

        int result = 1;

        for( int i = 10; i>=1; i-- ){

            result = result*i;
            // 1*10 = 10
            // 10 *9= 90
            // 90 * 8 =...



        }
        System.out.print(result);



    }



}
