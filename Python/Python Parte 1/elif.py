semana = int(input("Digite um número correspondente a um dia da semana: "))

if semana == 1:
    print("Domingo")
elif semana == 2:
    print("Segunda")
elif semana == 3:
    print("terça")
elif semana ==4:
    print("quarta")
elif semana == 5:
    print("quinta")
elif semana == 6:
    print("sexta")
elif semana == 7:
    print("sábado")
else:
    print("Número inválido!")